#include "libft.h"
#include <unistd.h>

void	print_32_bits(uint32_t b)
{
	t_byte	i;
	t_byte	b_towrite;

	i = 0;
	while (i < 31)
	{
		b_towrite = (b >> (31 - i)) % 2 + '0';
		write(1, &b_towrite, 1);
		i++;
	}
}
