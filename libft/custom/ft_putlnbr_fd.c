#include "libft.h"
#include <unistd.h>

void	ft_putlnbr_fd(long n, int fd)
{
	char	c;

	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
		ft_putlnbr_fd(n / 10, fd);
	c = n % 10 + 48;
	write(fd, &c, 1);
}
